int	ft_atoi(const char *str)
{
	int	i;
	int	n;
	int	flag;

	i = 0;
	n = 0;
	flag = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			flag = 1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		n = (n * 10) + (str[i] - '0');
		i++;
	}
	if (flag == 1)
	{
		n = -n;
	}
	return (n);
}
