int	ft_isalnum(int c)
{
	int	b;

	b = 0;
	if (((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'))
		|| (c >= '0' && c <= '9'))
	{
		b = 1;
	}
	return (b);
}
